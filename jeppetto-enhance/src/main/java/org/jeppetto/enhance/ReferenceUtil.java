package org.jeppetto.enhance;


/**
 * Utility for converting primitive types to objects.
 */
public class ReferenceUtil {

    //-------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------

    private ReferenceUtil() { /* do not instantiate */ }


    //-------------------------------------------------------------
    // Methods - Public - Static
    //-------------------------------------------------------------

    public static Object asObject(Object obj) {
        return obj;
    }


    public static Object asObject(boolean obj) {
        return obj;
    }


    public static Object asObject(byte obj) {
        return obj;
    }


    public static Object asObject(short obj) {
        return obj;
    }


    public static Object asObject(int obj) {
        return obj;
    }


    public static Object asObject(long obj) {
        return obj;
    }


    public static Object asObject(float obj) {
        return obj;
    }


    public static Object asObject(double obj) {
        return obj;
    }
}
